#风险等级判断函数（基于时间决策理论）
def get_risk_level(answers, w1=0.4, w2=0.3, w3=0.3):
    """
    根据时间决策理论计算拖延风险等级
    answers:dict,包含三个键:
        -"任务厌恶"(0-100)
        -"结果价值"(0-100)
        -"自我控制"(0-100)
    w1,w2,w3:自定义权重，默认为0.4,0.3,0.3
    返回:"高"/"中"/"低"和score值
    """
    score=(
        answers["任务厌恶"]*w1+
        (100-answers["结果价值"])*w2+
        (100-answers["自我控制"])*w3
    )
    if score<30:
        return "低", score
    elif score<70:
        return "中", score
    else:
        return "高", score

# 计算分数函数（用于单独计算分数）
