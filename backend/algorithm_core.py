    """
    计算拖延风险分数
    answers:dict,包含三个键:
        -"任务厌恶"(0-100)
        -"结果价值"(0-100)
        -"自我控制"(0-100)
    w1,w2,w3:自定义权重，默认为0.4,0.3,0.3
    返回:score值
    """
    return (
        answers["任务厌恶"]*w1+
        (100-answers["结果价值"])*w2+
        (100-answers["自我控制"])*w3
    )


